
# Делаем списки! == несколько элементов как один

l = [1, 2, 3, 4, 5] # Список
l[0] # 1
l[0:5] # [1, 2, 3, 4, 5]
l[::-1] # [5, 4, 3, 2, 1]

ll = [1, 2, 3, "4", "fabac", ["acasd", 3213412]]
print(ll[5][1]) # 3213412

