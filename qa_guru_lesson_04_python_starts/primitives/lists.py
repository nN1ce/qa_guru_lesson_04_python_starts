# Множества

s = {1, 2, 3, 3, 3, 3, 3}
s2 = {3, 4, 5}

print(s & s2) # {3} общая часть множества
print(s | s2)  # {1, 2, 3, 4, 5} # объединение множеств

l = [1, 2, 3, 3, 3, 4, 5]
l = list(set(l)) # list - список, set - множество
print(l) # [1, 2, 3, 4, 5]
